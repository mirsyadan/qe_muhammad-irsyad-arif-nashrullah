#!/bin/sh

# Menampilkan tanggal dan waktu saat ini
echo "Tanggal dan Waktu Sekarang:"
date

# Menampilkan informasi sistem
echo "Informasi Sistem:"
uname -a

# Menampilkan pengguna yang saat ini masuk
echo "Pengguna yang Masuk:"
whoami

# Menampilkan daftar file dalam direktori saat ini
echo "Daftar File dalam Direktori Saat Ini:"
ls

# Menampilkan ruang disk yang digunakan dan tersisa
echo "Ruang Disk yang Digunakan dan Tersisa:"
df -h

